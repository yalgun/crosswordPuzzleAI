    private void cozButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cozButtonActionPerformed
        String[][] newTable = new String[N][N];
        Search.N = N;
        try {
            newTable = Search.kurVeCoz(getArray(liste));
            for(int i=0;i<N;i++){
                for(int j=0;j<N;j++){
                    if(newTable[i][j].equals("/")){
                        liste.get(i).get(j).setBackground(Color.BLACK);
                    } else {    
                        liste.get(i).get(j).setText(newTable[i][j]);
                    }
                }
            }
        } catch (IOException ex) {
            olusturButtonActionPerformed(evt);
            cozButtonActionPerformed(evt);
        }
    }//GEN-LAST:event_cozButtonActionPerformed

    private void randomButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_randomButtonActionPerformed
        for(int i = 0;i<(N);i++){
            int randX = (int) (Math.random()*N);
            int randY = (int) (Math.random()*N);
            liste.get(randX).get(randY).setBackground(Color.BLACK);
            liste.get(randX).get(randY).setText("/");
        }
    }//GEN-LAST:event_randomButtonActionPerformed

    public void gridLayout(){
        TablePanel.removeAll();
        TablePanel.setLayout(new GridLayout(N, N, 0, 0));
        liste = new ArrayList<>();
        for(int i = 0;i<N;i++){
            ArrayList<JTextField> altListe = new ArrayList<>();
            for(int j = 0;j<N;j++){
                altListe.add(new JTextField());
                TablePanel.add(altListe.get(j));
            }
            liste.add(altListe);
        }
        pack();
    }
    
    public String[][] getArray(ArrayList<ArrayList<JTextField>> liste){
        String[][] TableArray = new String[liste.size()][liste.size()];
        for(int i=0;i<liste.size();i++){
            for(int j=0;j<liste.size();j++){
                 String temp= liste.get(i).get(j).getText().trim();
                 if(temp.length() == 0){
                     temp = "-";
                 }
                 TableArray[i][j] = temp;
            }
        }
        return TableArray;
    }
